package cat.nigga.module;

/**
 * Enumeration of different categories for Frost modules.
 */
public enum FrostModuleCategory {
    /**
     * Category for combat-related modules.
     */
    COMBAT,
    /**
     * Category for movement-related modules.
     */
    MOVEMENT,
    /**
     * Category for player-related modules.
     */
    PLAYER,
    /**
     * Category for render-related modules.
     */
    RENDER
}
